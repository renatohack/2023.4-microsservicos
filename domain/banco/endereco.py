class Endereco:
    
    def __init__(self, pais, cep, estado, cidade, bairro, rua, numero, complemento):
        self.pais = pais
        self.cep = cep
        self.estado = estado
        self.cidade = cidade
        self.bairro = bairro
        self.rua = rua
        self.numero = numero
        self.complemento = complemento